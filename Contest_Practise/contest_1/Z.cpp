void solve() {

    int n = in_i;
    ll w[n],h[n]; 

    for(int i=0; i<n; i++) {
        w[i] = in_ll;
        h[i] = in_ll;
    }

    if(h[0]<w[0])
        swap(w[0],h[0]);

    for(int i=1; i<n; i++) {

        if(h[i]<w[i])
            swap(w[i],h[i]);
        
        if(h[i-1]<h[i]) {
            swap(w[i],h[i]);
            if(h[i-1]<h[i]){
                NO;
                return;
            }
        }  
    }

    YES;




}

